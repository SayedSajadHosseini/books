     * @param  \App\Level  $level
     * @return \Illuminate\Http\Response
     */
    public function show(Level $level)
    {
        $levels = Level::all();
        foreach ($levels as $each_level) {
            $each_level->width = ($each_level->order * 15) > 100 ? 100 : $each_level->order * 15;
        }

        return view('levels.show', [
            'level' => $level,
            'levels' => $levels,
        ]);
    }

